// Introduction to JAVA Variables
/*
Types of Variables : 
1) Primitive Variables                        | 2) Non-Primitive Types
-> byte       {takes 1 byte of space}         | -> Strings (immutable)
-> short      {takes 2 bytes of space}        | -> Arrays
-> int        {takes 4 bytes of space}        |
-> long       {takes 8 bytes of space}        |
-> float      {takes 4 bytes of space}        |
-> double     {takes 8 bytes of space}        |
-> char       {takes 2 bytes of space}        |
-> boolean    {takes 1 byte of space}         |
 */

public class Variables {
  public static void main(String[] args) {
    // Primitive
    byte age = 30;
    int number = 1234567890;
    long number2 = 123456789089210L; // write letter "L" so jre recognises it as long variable tupe
    float pi = 3.14F; // same with float
    double accelearation = 9.8;
    char dollar = '$';
    boolean isAdult = true;

  }
}
