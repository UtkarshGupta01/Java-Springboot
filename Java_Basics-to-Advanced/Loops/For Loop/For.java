// Introduction to For Loops

public class For {
  public static void main(String[] args) {
    for (int i = 1; i <= 10; i++) {
      System.out.println(i);
    }

    // Example

    int n = 23;
    System.out.println("Table of 23 : ");
    for (int i = 1; i <= 10; i++) {
      System.out.println(n + " x " + i + " = " + n * i);
    }
  }

}
