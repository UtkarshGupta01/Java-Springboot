// Introduction to While Loop

public class While {
  public static void main(String[] args) {
    int i = 1;
    while (i < 10) {
      System.out.println(i);
      i++;
    }

    // Example

    int n = 19;
    i = 1;
    System.out.println("Table of 19 : ");

    while (i <= 10) {
      System.out.println(n + " x " + i + " = " + n * i);
      i++;
    }
  }

}
